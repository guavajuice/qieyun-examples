function 音變(音節) {
  const hasᅵ = "ᅵᅭᅣᅲᅧᅨ".includes(音節.中聲);
  function 替換(key, rule, condition = true) {
    if (condition)
      Object.entries(rule).forEach(([k, v]) => {
        音節[key] = 音節[key].replace(k, v);
      });
  }
  替換(
    "初聲",
    { ᄏ: "ᄀ", ᄐ: "ᄃ", ᄑ: "ᄇ", ᄎ: "ᄌ" },
    !選項.使用激音表示次清聲母
  );
  替換("初聲", {
    ᄁ: "ᄀ",
    ᄄ: "ᄃ",
    ᄈ: "ᄇ",
    ᄍ: "ᄌ",
    ᄊ: "ᄉ",
    ᅘ: "ᄒ",
    ᅌ: "ᄋ",
    ᅙ: "ᄋ",
    ᅀ: "ᄋ",
  });
  替換("中聲", { ᆞ: "ᅡ", ᆡ: "ᅢ", ᆔ: "ᅲ", ᆑ: "ᅧ", ᆒ: "ᅨ" });
  替換("終聲", { ᇢ: "", ᆼ: "", ᇰ: "ᆼ", ᇙ: "ᆯ" });
  替換("初聲", { ᄅ: "ᄂ" }, 選項.應用頭音法則);
  替換("初聲", { ᄂ: "ᄋ" }, hasᅵ && 選項.應用頭音法則);
  替換("初聲", { ᄃ: "ᄌ", ᄐ: "ᄎ" }, hasᅵ);
  替換(
    "中聲",
    { ᅭ: "ᅩ", ᅣ: "ᅡ", ᅲ: "ᅮ", ᅧ: "ᅥ", ᅨ: "ᅦ" },
    "ᄌᄎᄉ".includes(音節.初聲)
  );
  替換("中聲", { ᅳ: "ᅮ" }, "ᄇᄆ".includes(音節.初聲));
  替換("中聲", { ᅴ: "ᅵ" }, !"ᄒᄋ".includes(音節.初聲));
  替換("中聲", { ᅪ: "ᅡ", ᅫ: "ᅢ" }, !"ᄀᄏᄒᄋ".includes(音節.初聲));
  替換("中聲", { ᅱ: "ᅮ" }, 音節.終聲);
}

