function 聲調() {
  if (is推導現代) {
    if (!選項.標記長音 || 選項.注音方案 === "文觀部轉寫") return "";
    return {
      諺文: { 平: "", 上: "ː", 去: "ː", 入: "" },
      耶魯轉寫: { 平: "", 上: "̄", 去: "̄", 入: "" },
    }[選項.注音方案][音韻地位.聲];
  }
  return {
    諺文: { 平: "", 上: "〯", 去: "〮", 入: "〮" },
    耶魯轉寫: { 平: "̀", 上: "̌", 去: "́", 入: "́" },
  }[選項.注音方案][音韻地位.聲];
}

音節 = 音節.初聲 + 音節.中聲 + 音節.終聲;
if (選項.注音方案 !== "諺文")
  Object.entries(轉寫[選項.注音方案]).forEach(([k, v]) => {
    音節 = 音節.replace(k, v);
  });
if (選項.注音方案 === "耶魯轉寫") {
  if (is推導現代) {
    音節 = 音節.replace("wo", "o");
    if (is`脣音` || 音節.includes("ywu")) 音節 = 音節.replace("wu", "u");
    音節 = 音節.replace("wuy", "wi");
  }
  return 音節.replace(/.*[aeiou]/, "$&" + 聲調());
}
return 音節 + 聲調();
